            <div class="header">
                <h3 class="onas">Growth and expansion</h3>
            </div>
            <p class="align-content-center onas text-black">
                Fortinet became profitable in the third quarter of 2008. Later that year, the company acquired the intellectual
                property of IPLocks, a database security and auditing company. In August 2009, Fortinet acquired the intellectual property
                and other assets of Woven Systems, an ethernet switching company.
                According to market research firm IDC, by November 2009 Fortinet held over 15 percent of the unified threat management
                market.[16] Also in 2009, CRN Magazine's survey-based annual report card placed Fortinet first in network security hardware,
                up from seventh in 2007. In November 2009, Fortinet had an initial public offering. By the end of the first day of trading the
                company had raised $156 million in financing.
                By 2010, Fortinet had $324 million in annual revenues and held the largest share of the unified threat management market
                according to IDC. Fortinet made four notable acquisitions from 2012 to 2016. The company acquired app-hosting service XDN
                (formerly known as 3Crowd) in December 2012, Coyote Point in 2013, and Wi-Fi hardware company Meru Networks in 2015. In June 2016,
                Fortinet acquired IT security, monitoring and analytics software vendor, AccelOps.
            </p>
        </div>
        <div class="col-xl-6 p-3 text-black bg-light">

            <p>
                In July 2014, Fortinet announced a technical certification program called the Network Security Expert (NSE) program. In March
                2016, Fortinet launched a Network Security Academy to help fill open cyber security jobs in the U.S. Fortinet donated equipment and
                provided information to universities to help train students for jobs in the field. Also in 2016, Fortinet launched a program
                called FortiVet to recruit military veterans for cybersecurity jobs. In January 2017, it was announced that Philip Quade,
                a former member of the NSA, would become the company's chief information security officer. At the end of 2017, Fortinet
                reported $416.7 million in revenue, a 15 percent increase from the previous year. In June 2018, Fortinet acquired Bradford
                Networks, a maker of access control and IoT security solutions. In October 2018, Fortinet acquired ZoneFox, a threat
                analytics company. In January 2019, it was announced that Fortinet and founder Ken Xie would participate in the annual
                World Economic Forum held in Davos, Switzerland. In September 2019, Fortinet settled a whistleblower lawsuit regarding
                what the company has described as an "isolated incident" of sales of intentionally mislabeled Chinese-made equipment
                to U.S. government end users. In late 2019, Fortinet acquired enSilo and CyberSponse. Also in 2019, Fortinet's FortiGate
                SD-WAN and Next Generation Firewall received a "Recommended" rating from NSS Labs.

                In July 2020, Fortinet acquired OPAQ Networks.[37] OPAQ is a Secure Access Service Edge (SASE) cloud provider based in Herndon, Virginia.
            </p>

