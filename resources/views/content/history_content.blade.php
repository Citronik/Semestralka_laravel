        <div class="header">
            <p class="align-content-center onas text-black">
                In 2000, Ken Xie and his brother Michael Xie co-founded Appligation Inc.
                The company was renamed to ApSecure in December 2000 and later renamed again to Fortinet, based on the phrase
                "Fortified Networks."
                Fortinet introduced its first product, FortiGate, in 2002, followed by anti-spam and anti-virus software.
                The company raised $13 million in private funding from 2000 to early 2003. Fortinet's first channel program was
                established in October 2003. The company began distributing its products in Canada in December 2003,
                and in the UK in February 2004. By 2004 Fortinet had offices in Asia, Europe and North America.
                In April 2005, a German court issued a preliminary injunction against Fortinet's UK subsidiary
                in relation to source code for its GPL-licensed elements. The dispute ended a month later after Fortinet
                agreed to make the source code available upon request.
            </p>
